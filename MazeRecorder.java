public class MazeRecorder {
    private int heightY;
    private int widthX;
    public MazeRecorder(int height, int width) {
        this.heightY = height;
        this.widthX = width;
    }
    public int getHeightY() {
        return this.heightY;
    }
    public int getWidthX() {
        return this.widthX;
    }
}
